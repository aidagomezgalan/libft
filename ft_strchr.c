#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != (char) c)
	{
		if (!s[i]) //if (s[i] == '\0')
			return (NULL);
		s++;
	}
	return ((char *)&s[i]);
}
